<?php

namespace App\Entity;

use App\Repository\CartRepository;
use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity(repositoryClass=CartRepository::class)
 */
class Cart
{
    /**
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $Name;

    /**
     * @ORM\ManyToOne(targetEntity=CartCollection::class, inversedBy="carts")
     * @ORM\JoinColumn(nullable=false)
     */
    private $CartCollectionId;

    /**
     * @ORM\Column(type="string", length=510, nullable=true)
     */
    private $img;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getName(): ?string
    {
        return $this->Name;
    }

    public function setName(string $Name): self
    {
        $this->Name = $Name;

        return $this;
    }

    public function getCartCollectionId(): ?CartCollection
    {
        return $this->CaetCollectionId;
    }

    public function setCartCollectionId(?CartCollection $CaetCollectionId): self
    {
        $this->CaetCollectionId = $CaetCollectionId;

        return $this;
    }

    public function getImg(): ?string
    {
        return $this->img;
    }

    public function setImg(?string $img): self
    {
        $this->img = $img;

        return $this;
    }
}
